def apply_wizard_state_to_config(
    state: WizardState,
    config: ArchConfig,
    mirror_list_handler: MirrorListHandler,
) -> None:
    """Apply wizard selections to ArchConfig."""
    # Locale
    config.locale_config = LocaleConfiguration(
        kb_layout='',
        sys_lang=state.locale,
        sys_enc='UTF-8',
    )

    # Timezone
    if state.country and state.country in COUNTRY_TIMEZONES:
        config.timezone = COUNTRY_TIMEZONES[state.country]

    # Mirror regions (with fallback pools)
    new_mirror = build_mirror_config(state.country, mirror_list_handler)
    if config.mirror_config is None:
        config.mirror_config = new_mirror
    else:
        config.mirror_config.mirror_regions = new_mirror.mirror_regions
        config.mirror_config.custom_servers = new_mirror.custom_servers

    # Multilib
    if state.multilib:
        if Repository.Multilib not in config.mirror_config.optional_repositories:
            config.mirror_config.optional_repositories.append(Repository.Multilib)
    else:
        config.mirror_config.optional_repositories = [
            r for r in config.mirror_config.optional_repositories
            if r != Repository.Multilib
        ]

    # Disk layout
    if state.disk_device:
        config.disk_config = build_disk_layout(state.disk_device)
        # Add @swap subvolume for hibernation (no compression — swap on
        # compressed btrfs is problematic)
        if state.hibernation and config.disk_config:
            for dev_mod in config.disk_config.device_modifications:
                for part in dev_mod.partitions:
                    if part.btrfs_subvols:
                        part.btrfs_subvols.append(
                            SubvolumeModification(Path('@swap'), Path('/swap')),
                        )
                        break

    # Network
    config.network_config = NetworkConfiguration(type=state.network_type)

    # Hostname
    config.hostname = state.hostname

    # GPU packages
    gpu_packages = list(GPU_PACKAGES.get('common', []))  # always include mesa
    for vendor in state.gpu_vendors:
        gpu_packages.extend(GPU_PACKAGES.get(vendor, []))
    # Merge with base packages (avoid duplicates)
    all_packages = list(BASE_PACKAGES)
    for pkg in gpu_packages:
        if pkg not in all_packages:
            all_packages.append(pkg)
    # kmscon for non-English locales
    if needs_kmscon(state.locale) and 'kmscon' not in all_packages:
        all_packages.append('kmscon')
    # kmscon font package
    if state.kmscon_font_package and state.kmscon_font_package not in all_packages:
        all_packages.append(state.kmscon_font_package)
    # Browser packages (pacman only; AUR browsers handled in post-install)
    for browser_key in state.browsers:
        browser_info = BROWSER_OPTIONS.get(browser_key, {})
        if not browser_info.get('aur', False):
            pkg = browser_info.get('package', '')
            if pkg and pkg not in all_packages:
                all_packages.append(pkg)
    # upower for desktop environments that require it
    if state.desktop_env in ('dms', 'dms_manual', 'exo') and 'upower' not in all_packages:
        all_packages.append('upower')
    # btrfs-assistant and gsmartcontrol for non-minimal desktops
    if state.desktop_env != 'minimal':
        if 'btrfs-assistant' not in all_packages:
            all_packages.append('btrfs-assistant')
        if 'gsmartcontrol' not in all_packages:
            all_packages.append('gsmartcontrol')
    # reflector for non-CN users (mirror auto-update)
    if state.country != 'CN' and 'reflector' not in all_packages:
        all_packages.append('reflector')
    # Terminal enhancement
    all_packages.extend(TERMINAL_ENHANCEMENT_PACKAGES)

    # Clipboard support (Wayland extras when a desktop is selected)
    all_packages.extend(CLIPBOARD_PACKAGES)
    if state.desktop_env != 'minimal':
        all_packages.extend(CLIPBOARD_WAYLAND_PACKAGES)
        # Note: CLIPBOARD_WAYLAND_AUR_PACKAGES (comalot-clipsync-git) handled in installation.py

    # Archive extraction
    all_packages.append('unzip')

    # Input method packages
    for im_key in state.input_methods:
        if im_key in INPUT_METHOD_PACKAGES:
            all_packages.extend(INPUT_METHOD_PACKAGES[im_key]['packages'])

    # Font packages
    for font_key in state.base_fonts:
        if font_key in BASE_FONT_OPTIONS:
            all_packages.extend(BASE_FONT_OPTIONS[font_key]['packages'])
    for font_key in state.nerd_fonts:
        if font_key in NERD_FONT_OPTIONS:
            all_packages.extend(NERD_FONT_OPTIONS[font_key]['packages'])

    # Audio firmware packages
    for af_key in state.audio_firmware:
        if af_key in AUDIO_FIRMWARE_OPTIONS:
            all_packages.extend(AUDIO_FIRMWARE_OPTIONS[af_key]['packages'])

    # Polkit agent
    if state.polkit_agent and state.polkit_agent in POLKIT_AGENT_OPTIONS:
        all_packages.extend(POLKIT_AGENT_OPTIONS[state.polkit_agent]['packages'])

    # Keyring
    if state.keyring and state.keyring in KEYRING_OPTIONS:
        all_packages.extend(KEYRING_OPTIONS[state.keyring]['packages'])

    # File managers (non-AUR only; AUR ones handled in installation.py)
    for fm_key in state.file_managers:
        if fm_key in FILE_MANAGER_OPTIONS and not FILE_MANAGER_OPTIONS[fm_key].get('aur', False):
            all_packages.extend(FILE_MANAGER_OPTIONS[fm_key]['packages'])

    # Minimal install: default to yazi as file manager
    if state.desktop_env == 'minimal':
        all_packages.append('yazi')

    # Remote desktop (non-AUR only)
    for rd_key in state.remote_desktop:
        if rd_key in REMOTE_DESKTOP_OPTIONS and not REMOTE_DESKTOP_OPTIONS[rd_key].get('aur', False):
            all_packages.extend(REMOTE_DESKTOP_OPTIONS[rd_key]['packages'])

    # Dev environment packages (non-AUR only)
    for de_key in state.dev_environments:
        if de_key in DEV_ENVIRONMENT_OPTIONS and not DEV_ENVIRONMENT_OPTIONS[de_key].get('aur', False):
            all_packages.extend(DEV_ENVIRONMENT_OPTIONS[de_key]['packages'])

    # Gaming packages (non-AUR only)
    for g_key in state.gaming_tools:
        if g_key in GAMING_OPTIONS and not GAMING_OPTIONS[g_key].get('aur', False):
            all_packages.extend(GAMING_OPTIONS[g_key]['packages'])

    # VM packages (non-AUR only)
    for vm_key in state.vm_options:
        if vm_key in VM_OPTIONS and not VM_OPTIONS[vm_key].get('aur', False):
            all_packages.extend(VM_OPTIONS[vm_key]['packages'])

    # Deduplicate packages while preserving order
    config.packages = list(dict.fromkeys(all_packages))

    # Authentication
    users = []
    if state.username and state.user_password:
        users.append(User(
            username=state.username,
            password=state.user_password,
            sudo=True,
        ))
    config.auth_config = AuthenticationConfiguration(
        root_enc_password=state.root_password,
        users=users,
    )

    # Custom commands for kmscon setup
    if needs_kmscon(state.locale):
        if state.desktop_env in ('dms', 'dms_manual', 'exo'):
            # Graphical DEs use greetd on tty1; put kmscon on tty2 to avoid
            # DRM master contention (kmscon holds /dev/dri/card* open).
            config.custom_commands = [
                'systemctl disable getty@tty2',
                'systemctl enable kmsconvt@tty2',
            ]
        else:
            config.custom_commands = [
                'systemctl disable getty@tty1',
                'systemctl enable kmsconvt@tty1',
            ]
    else:
        config.custom_commands = []

    # Services (empty — handled by app_config and profile)
    config.services = []
