def generate_kmscon_config(
    font_name: str,
    font_size: int,
    has_gpu: bool,
) -> str:
    """Generate /etc/kmscon/kmscon.conf content based on detected environment.

    Args:
        font_name: Font family name (e.g. 'Noto Sans CJK SC').
        font_size: Calculated font size based on screen resolution.
        has_gpu: Whether a discrete/integrated GPU was detected (enables hwaccel).
    """
    lines = [
        '# Generated by arch-bootstrap',
        '# See kmscon.conf(5) for all available options',
        '',
        '### Graphics Options',
        'drm',
        *(['hwaccel'] if has_gpu else ['no-hwaccel']),
        '',
        '### Font Options',
        'font-engine=pango',
        f'font-name={font_name}',
        f'font-size={font_size}',
        '',
        '### Terminal Options',
        'term=xterm-256color',
        'sb-size=10000',
        '',
    ]
    return '\n'.join(lines)


