def _fontconfig_match_alias(from_name: str, to_name: str) -> str:
    """Generate a fontconfig <match> block that aliases one font family to another."""
    escaped_from = xml_escape(from_name)
    escaped_to = xml_escape(to_name)
    return (
        '  <match target="pattern">\n'
        '    <test qual="any" name="family">\n'
        f'      <string>{escaped_from}</string>\n'
        '    </test>\n'
        '    <edit name="family" mode="assign" binding="same">\n'
        f'      <string>{escaped_to}</string>\n'
        '    </edit>\n'
        '  </match>'
    )


def _fontconfig_generic_override(generic: str, font_name: str) -> str:
    """Generate a fontconfig <match> that prepends a font to a generic family."""
    return (
        '  <match target="pattern">\n'
        '    <test qual="any" name="family">\n'
        f'      <string>{generic}</string>\n'
        '    </test>\n'
        '    <edit name="family" mode="prepend" binding="strong">\n'
        f'      <string>{font_name}</string>\n'
        '    </edit>\n'
        '  </match>'
    )


def _fontconfig_lang_override(lang: str, generic: str, font_name: str) -> str:
    """Generate a fontconfig <match> that prepends a font for a specific language."""
    return (
        '  <match>\n'
        '    <test name="lang" compare="contains">\n'
        f'      <string>{lang}</string>\n'
        '    </test>\n'
        '    <test name="family">\n'
        f'      <string>{generic}</string>\n'
        '    </test>\n'
        '    <edit name="family" mode="prepend">\n'
        f'      <string>{font_name}</string>\n'
        '    </edit>\n'
        '  </match>'
    )


def generate_fontconfig(cjk_font_name: str, locale: str, nerd_font_families: list[str] | None = None) -> str:
    """Generate ~/.config/fontconfig/fonts.conf for CJK locale users.

    Sets the selected CJK font as the preferred font for the locale's language,
    disables embedded bitmap fonts, and aliases common CJK font names to the
    selected font so applications requesting unavailable fonts degrade gracefully.

    Args:
        cjk_font_name: The CJK font family selected by the user (e.g. 'Noto Sans CJK SC').
        locale: System locale (e.g. 'zh_CN.UTF-8').
    """
    # Determine language code for fontconfig lang matching
    lang = locale.split('_')[0]  # 'zh' or 'ja'

    # Derive monospace CJK variant from explicit mapping.
    # Fonts without a mono variant in the same package won't override monospace.
    mono_cjk_font_name = FONTCONFIG_CJK_MONO.get(cjk_font_name)

    sections: list[str] = [
        '<?xml version="1.0"?>',
        '<!DOCTYPE fontconfig SYSTEM "fonts.dtd">',
        '<!-- Generated by arch-bootstrap -->',
        '<fontconfig>',
        '',
        '  <!-- Disable embedded bitmap fonts -->',
        '  <match target="font">',
        '    <edit name="embeddedbitmap" mode="assign">',
        '      <bool>false</bool>',
        '    </edit>',
        '  </match>',
        '',
        '  <!-- CJK font defaults for serif/sans-serif/monospace -->',
        _fontconfig_lang_override(lang, 'serif', cjk_font_name),
        _fontconfig_lang_override(lang, 'sans-serif', cjk_font_name),
    ]

    if nerd_font_families:
        for nf in nerd_font_families:
            sections.append(_fontconfig_generic_override('monospace', nf))
    if mono_cjk_font_name:
        sections.append(_fontconfig_lang_override(lang, 'monospace', mono_cjk_font_name))

    # Font alias mappings: redirect common CJK font names to the selected font
    aliases = FONTCONFIG_CJK_ALIASES.get(lang, [])
    if aliases:
        sections.append('')
        sections.append(f'  <!-- Alias common {lang.upper()} fonts to {cjk_font_name} -->')
        for alias_name in aliases:
            sections.append(_fontconfig_match_alias(alias_name, cjk_font_name))

    sections.extend([
        '',
        '</fontconfig>',
        '',
    ])

    return '\n'.join(sections)


